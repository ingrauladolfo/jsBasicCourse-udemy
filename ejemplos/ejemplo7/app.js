'use strict'
var num1, num2;
num1 = 1;
num2 = 5;
//Operaciones básicas
var resultadosuma = num1 + num2;
var resultadoresta = num1 - num2;
var resultadodivision = num1 / num2;
var resultadomultiplicacion = num1 * num2;

console.log('El resultado de la suma es: ' + resultadosuma)
console.log('El resultado de la resta es: ' + resultadoresta)
console.log('El resultado de la división es: ' + resultadodivision)
console.log('El resultado de la multiplicación es: ' + resultadomultiplicacion)

//Operadores de incremento/decremento
resultadosuma ++;
console.log('Incrementando: '+ resultadosuma);
resultadosuma --;
console.log('Decrementando: '+ resultadoresta);